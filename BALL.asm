DRAW_BALL PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	
	; select pallete
    MOV AH,0Bh
	MOV BH,1
	MOV BL,1
	
	INT 10h   
	; writing pixel
	
	MOV AH,0Ch
	MOV AL, 3
	
	SUB CX,3	
	INT 10h
	
	DEC DX
	INT 10H
	
	DEC DX
	INC CX
	INT 10H
	
	DEC DX
	INC CX
	INT 10H
	
	INC CX
	INT 10H
	
	INC CX
	INT 10H
	
	INC CX
	INC DX
	INT 10H
	
	INC CX
	INC DX
	INT 10H
	
	INC DX
	INT 10H
	
	INC DX
	INT 10H
	
	DEC CX
	INC DX
	INT 10H
	
	DEC CX
	INC DX
	INT 10H
	
	DEC CX
	INT 10H
	
	DEC CX
	INT 10H
	
	DEC CX
	DEC DX
	INT 10H
	
	DEC CX
	DEC DX
	INT 10H
	
	POP DX
	POP CX
	POP BX
	POP AX
	
	RET	
	
DRAW_BALL ENDP


