MOVE_BALL PROC
	
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	
	LPOUTER:
		CALL DRAW_BALL
		
		PUSH AX
		MOV AX, 10000
		LP1:
			DEC AX
			CMP AX,0 
			JNLE LP1
		POP AX	
		
		CALL ERASE_BALL
		INC CX
		CMP CX, 240
		JG DONE
		JMP LPOUTER	
	
	
DONE:
	CALL DRAW_BALL
	POP DX
	POP CX
	POP BX
	POP AX
	RET
MOVE_BALL ENDP